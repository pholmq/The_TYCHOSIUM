//TIME CONSTANTS
export const YEAR_LENGTH = 365.2425;
export const EARTH_ROTATIONS = 366.2425;

export const DAY = 1 / YEAR_LENGTH;
export const YEAR = DAY * 365;
export const MONTH = DAY * 30;
export const WEEK = DAY * 7;
export const HOUR = DAY / 24;
export const MINUTE = HOUR / 60;
export const SECOND = MINUTE / 60;
